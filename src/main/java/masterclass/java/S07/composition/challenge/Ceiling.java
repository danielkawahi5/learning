package masterclass.java.S07.composition.challenge;

public class Ceiling {

    private int height;
    private int ceilingColor;

    public Ceiling(int height, int ceilingColor) {
        this.height = height;
        this.ceilingColor = ceilingColor;
    }

    public int getHeight() {
        return height;
    }

    public int getCeilingColor() {
        return ceilingColor;
    }
}
